// ฟังก์ชันสำหรับคัดลอกสูตรในหน้ารายละเอียด
function copyFormula(text) {
  const textArea = document.createElement("textarea");
  textArea.value = text;
  document.body.appendChild(textArea);
  textArea.select();
  try {
    document.execCommand('copy');
    const copyBtn = document.getElementById('copyBtn');
    const copyBtnText = document.getElementById('copyBtnText');
    
    if (copyBtn && copyBtnText) {
      copyBtn.classList.remove('bg-indigo-800/60');
      copyBtn.classList.add('bg-emerald-600', 'text-white');
      copyBtnText.innerText = 'คัดลอกแล้ว';
      
      setTimeout(() => {
        copyBtn.classList.remove('bg-emerald-600', 'text-white');
        copyBtn.classList.add('bg-indigo-800/60');
        copyBtnText.innerText = 'คัดลอกสูตร';
      }, 2000);
    }
  } catch (err) {
    console.error('ไม่สามารถคัดลอกสูตรได้', err);
  }
  document.body.removeChild(textArea);
}
