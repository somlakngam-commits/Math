// โหลดข้อมูลเมื่อหน้าเว็บพร้อมทำงาน
document.addEventListener('DOMContentLoaded', () => {
  const container = document.getElementById('detailContainer');
  if (!container) return;

  // 1. ดึง ID จาก URL parameters
  const urlParams = new URLSearchParams(window.location.search);
  const formulaId = parseInt(urlParams.get('id'));

  if (isNaN(formulaId)) {
    showError('ไม่พบรหัสสูตรที่ระบุ กรุณากลับไปที่หน้าหลัก');
    return;
  }

  // 2. ค้นหาสูตรในข้อมูล formulasData (ที่ถูกโหลดมาจาก script.js)
  // รอให้ script.js โหลดตัวแปรสำเร็จ
  if (typeof formulasData === 'undefined') {
    showError('เกิดข้อผิดพลาดในการโหลดคลังข้อมูลสูตร');
    return;
  }

  const formulaItem = formulasData.find(item => item.id === formulaId);

  if (!formulaItem) {
    showError('ไม่พบข้อมูลสูตรนี้ในระบบ อาจเนื่องมาจากรหัสสูตรไม่ถูกต้อง');
    return;
  }

  // 3. นำข้อมูลไปสร้างเป็น HTML
  renderDetailPage(formulaItem, container);
});

